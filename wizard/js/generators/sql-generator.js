/**
 * sql-generator.js — Generates the full Supabase setup SQL:
 *   CREATE TABLE, RPC functions (count, markers, details, per-filter), RLS, indexes.
 */

export function generateSetupSQL({ columns, filters, tableName }) {
    const pk = columns.find(c => c.role === 'primary_key')?.name || 'id';
    const latCol = columns.find(c => c.role === 'latitude')?.name || 'latitude';
    const lngCol = columns.find(c => c.role === 'longitude')?.name || 'longitude';
    const filterCols = filters.map(f => f.column);

    let sql = `-- WebGIS Setup SQL for "${tableName}"\n`;
    sql += `-- Generated by the WebGIS Setup Wizard\n\n`;

    // ── CREATE TABLE ──
    sql += `CREATE TABLE IF NOT EXISTS ${ident(tableName)} (\n`;
    const colDefs = columns.map(c => {
        let def = `    ${ident(c.name)} ${pgType(c.type)}`;
        if (c.role === 'primary_key') def += ' PRIMARY KEY GENERATED ALWAYS AS IDENTITY';
        return def;
    });
    sql += colDefs.join(',\n');
    sql += `\n);\n\n`;

    // ── RLS ──
    sql += `ALTER TABLE ${ident(tableName)} ENABLE ROW LEVEL SECURITY;\n`;
    sql += `CREATE POLICY "Allow public read" ON ${ident(tableName)} FOR SELECT USING (true);\n\n`;

    // ── Indexes ──
    sql += `CREATE INDEX IF NOT EXISTS idx_${tableName}_coords ON ${ident(tableName)} (${ident(latCol)}, ${ident(lngCol)});\n`;
    for (const col of filterCols) {
        sql += `CREATE INDEX IF NOT EXISTS idx_${tableName}_${col} ON ${ident(tableName)} (${ident(col)});\n`;
    }
    sql += '\n';

    // ── RPC: count ──
    sql += rpcCount(tableName);

    // ── RPC: markers ──
    sql += rpcMarkers(tableName, columns, filterCols, latCol, lngCol, pk);

    // ── RPC: details ──
    sql += rpcDetails(tableName, pk);

    // ── RPC: per-filter cross-filter functions ──
    for (const col of filterCols) {
        sql += rpcFilter(tableName, col, filterCols);
    }

    return sql;
}

/* ── RPC generators ──────────────────────────── */

function rpcCount(table) {
    return `
CREATE OR REPLACE FUNCTION api_${table}_count()
RETURNS bigint LANGUAGE sql STABLE AS $$
    SELECT count(*) FROM ${ident(table)};
$$;\n\n`;
}

function rpcMarkers(table, columns, filterCols, latCol, lngCol, pk) {
    const params = filterCols.map(c => `p_${c} text DEFAULT NULL`).join(', ');
    const markerCols = [pk, latCol, lngCol, ...filterCols].filter((v, i, a) => a.indexOf(v) === i);
    const selectCols = markerCols.map(c => ident(c)).join(', ');

    let where = filterCols.map(c =>
        `(p_${c} IS NULL OR ${ident(c)}::text = p_${c})`
    ).join('\n        AND ');

    return `
CREATE OR REPLACE FUNCTION api_${table}_markers(${params})
RETURNS TABLE(${markerCols.map(c => `${ident(c)} ${pgTypeFor(c, columns)}`).join(', ')})
LANGUAGE sql STABLE AS $$
    SELECT ${selectCols}
    FROM ${ident(table)}
    WHERE ${where || 'true'};
$$;\n\n`;
}

function rpcDetails(table, pk) {
    return `
CREATE OR REPLACE FUNCTION api_${table}_details(p_id bigint)
RETURNS SETOF ${ident(table)} LANGUAGE sql STABLE AS $$
    SELECT * FROM ${ident(table)} WHERE ${ident(pk)} = p_id;
$$;\n\n`;
}

function rpcFilter(table, column, allFilterCols) {
    const otherCols = allFilterCols.filter(c => c !== column);
    const params = otherCols.map(c => `p_${c} text DEFAULT NULL`).join(', ');
    let where = otherCols.map(c =>
        `(p_${c} IS NULL OR ${ident(c)}::text = p_${c})`
    ).join('\n        AND ');

    return `
CREATE OR REPLACE FUNCTION api_${table}_filter_${column}(${params})
RETURNS TABLE(${ident(column)} text) LANGUAGE sql STABLE AS $$
    SELECT DISTINCT ${ident(column)}::text AS ${ident(column)}
    FROM ${ident(table)}
    WHERE ${where || 'true'}
    ORDER BY ${ident(column)};
$$;\n\n`;
}

/* ── Helpers ──────────────────────────────────── */

function pgType(type) {
    const map = { text: 'text', integer: 'bigint', numeric: 'numeric', boolean: 'boolean', date: 'date', timestamp: 'timestamptz' };
    return map[type] || 'text';
}

function pgTypeFor(colName, columns) {
    const col = columns.find(c => c.name === colName);
    return col ? pgType(col.type) : 'text';
}

function ident(name) {
    // Only quote if needed (contains uppercase/special chars)
    if (/^[a-z_][a-z0-9_]*$/.test(name)) return name;
    return `"${name.replace(/"/g, '""')}"`;
}
